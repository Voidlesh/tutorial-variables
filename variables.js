const i = 'hello world';
console.log(i) //Constant Variable can't be reassigned hence the name const.
let i = '';
i = 'hello world';
console.log(i) //Block scoped variable, can be reassigned.
var i = '';
i = 'hello world';
console.log(i) //Var is used to make a normal variable, but both let and var work, I prefer let.
